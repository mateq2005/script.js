{
    //The While Loop
    while (condition) {
        // code block to be executed
    }
}

{
    //Example
    let i = 0;
    while (i < 10) {
        text += "The number is " + i;
        i++
    }
}

{
    //The Do While Loop
    do {
        // code block to be executed
    }
    while (condition);
}

{
    //Example
    let i = 0;
    do {
        text += "The number is" + i;
        i++;
    }
    while (i < 10)
}