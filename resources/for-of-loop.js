{
    //The For Of Loop
    for (variable of iterable) {
        // code block to be executed
    }
}

{
    //Looping over an Array
    const cars = ["BMW", "Volvo", "Mini"];
    let text = "";

    for (let x of cars) {
        text += x;
    }
}

{
    //Looping over a String
    let language = "JavaScript";
    let text = "";

    for (let x of language) {
        text += x;
    }
}
